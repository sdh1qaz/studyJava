public class SwitchCase {
	public static void main(String[] args) {
		String str = "9";
		switch(str) {
		case "2":
		case "3":
		case "4":
			System.out.println("ok");
			break;
		default:
			System.out.println("not ok");
		}
		System.out.println(1 + 8 + "5");
	}

}
